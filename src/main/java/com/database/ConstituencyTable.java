package com.database;


public class ConstituencyTable {

	int id_constituency;
	String constituency_name;
	
	public int getId_constituency() {
		return id_constituency;
	}
	public void setId_constituency(int id_constituency) {
		this.id_constituency = id_constituency;
	}
	public String getConstituency_name() {
		return constituency_name;
	}
	public void setConstituency_name(String constituency_name) {
		this.constituency_name = constituency_name;
	}
	
	
	

}
